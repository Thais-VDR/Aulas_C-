using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace aula2
{
   public class Calculadora
    {
        public int Soma(int op1, int op2)
        {
            return op1 + op2;
        }
        public int Subtrair(int op1, int op2)
        {
            return op1 - op2;
        }
        public int Multiplicacao(int op1, int op2)
        {
            return op1 * op2;

        }
        public int Divisao(int op1, int op2)
        {
            return op1 / op2;
        }
    }

}
